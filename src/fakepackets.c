/**
 * Generate a random unsigned int using getrandom() or /dev/urandom
 */
static int secure_random(unsigned int *result) {
    ssize_t ret = getrandom(result, sizeof(*result), 0);
    if (ret < 0 || (size_t)ret != sizeof(*result)) {
        /* Fallback to /dev/urandom */
        FILE *f = fopen("/dev/urandom", "r");
        if (!f) return -1;
        size_t rd = fread(result, sizeof(*result), 1, f);
        fclose(f);
        if (rd != 1) return -1;
    }
    return 0;
}

/**
 * Parse a raw packet and locate IP header, TCP header, and payload.
 * Returns 0 on success, -1 on failure.
 */
static int parse_packet(const char *pkt, unsigned int packetLen, int is_ipv6,
                        void **ip_hdr, struct tcphdr **tcp_hdr,
                        char **payload, unsigned int *payload_len)
{
    if (!is_ipv6) {
        if (packetLen < sizeof(struct iphdr))
            return -1;
        struct iphdr *iph = (struct iphdr *)pkt;
        unsigned int ip_hdr_len = (unsigned int)iph->ihl * 4;
        if (packetLen < ip_hdr_len + sizeof(struct tcphdr))
            return -1;

        struct tcphdr *tcph = (struct tcphdr *)(pkt + ip_hdr_len);
        unsigned int tcp_hdr_len = (unsigned int)tcph->doff * 4;
        unsigned int total_hdr = ip_hdr_len + tcp_hdr_len;

        *ip_hdr = iph;
        *tcp_hdr = tcph;
        if (packetLen > total_hdr) {
            *payload = (char *)(pkt + total_hdr);
            *payload_len = packetLen - total_hdr;
        } else {
            *payload = NULL;
            *payload_len = 0;
        }
        return 0;
    }
    else {
        if (packetLen < sizeof(struct ip6_hdr))
            return -1;
        struct ip6_hdr *ip6h = (struct ip6_hdr *)pkt;
        unsigned int ip6_hdr_len = sizeof(struct ip6_hdr);

        if (packetLen < ip6_hdr_len + sizeof(struct tcphdr))
            return -1;

        struct tcphdr *tcph = (struct tcphdr *)(pkt + ip6_hdr_len);
        unsigned int tcp_hdr_len = (unsigned int)tcph->doff * 4;
        unsigned int total_hdr = ip6_hdr_len + tcp_hdr_len;

        *ip_hdr = ip6h;
        *tcp_hdr = tcph;
        if (packetLen > total_hdr) {
            *payload = (char *)(pkt + total_hdr);
            *payload_len = packetLen - total_hdr;
        } else {
            *payload = NULL;
            *payload_len = 0;
        }
        return 0;
    }
}

/**
 * Send fake packet data using the original packet as a template.
 */
static int send_fake_data(const char *pkt,
                          const unsigned int packetLen,
                          const int is_ipv6,
                          const int is_https,
                          const uint8_t set_ttl,
                          const uint8_t set_checksum,
                          const uint8_t set_seq,
                          const struct fake_t *fake_data
                         ) {
    char packet_fake[MAX_PACKET_SIZE];
    void *ip_hdr_ptr;
    struct tcphdr *tcph;
    char *payload;
    unsigned int payload_len;

    unsigned const char *fake_request_data = is_https ? fake_https_request : fake_http_request;
    unsigned int fake_request_size = is_https ? sizeof(fake_https_request) : sizeof(fake_http_request) - 1;
    if (fake_data) {
        fake_request_data = fake_data->data;
        fake_request_size = (unsigned int)fake_data->size;
    }

    memcpy(packet_fake, pkt, packetLen);

    if (parse_packet(packet_fake, packetLen, is_ipv6, &ip_hdr_ptr, &tcph, &payload, &payload_len) < 0)
        return 1;

    if (packetLen + fake_request_size + 1 > MAX_PACKET_SIZE)
        return 2;

    /* Replace payload with fake data */
    unsigned int new_packetLen;
    if (!is_ipv6) {
        struct iphdr *iph = (struct iphdr *)ip_hdr_ptr;
        unsigned int ip_hdr_len = (unsigned int)iph->ihl * 4;
        unsigned int tcp_hdr_len = (unsigned int)tcph->doff * 4;
        char *data_start = packet_fake + ip_hdr_len + tcp_hdr_len;

        memcpy(data_start, fake_request_data, fake_request_size);
        new_packetLen = ip_hdr_len + tcp_hdr_len + fake_request_size;

        iph->tot_len = htons((uint16_t)new_packetLen);

        if (set_ttl)
            iph->ttl = set_ttl;
    }
    else {
        struct ip6_hdr *ip6h = (struct ip6_hdr *)ip_hdr_ptr;
        unsigned int ip6_hdr_len = sizeof(struct ip6_hdr);
        unsigned int tcp_hdr_len = (unsigned int)tcph->doff * 4;
        char *data_start = packet_fake + ip6_hdr_len + tcp_hdr_len;

        memcpy(data_start, fake_request_data, fake_request_size);
        new_packetLen = ip6_hdr_len + tcp_hdr_len + fake_request_size;

        ip6h->ip6_plen = htons((uint16_t)(tcp_hdr_len + fake_request_size));

        if (set_ttl)
            ip6h->ip6_hlim = set_ttl;
    }

    if (set_seq) {
        /* Shift ACK and SEQ numbers to confuse conntrack */
        tcph->ack_seq = htonl(ntohl(tcph->ack_seq) - 66000);
        tcph->seq = htonl(ntohl(tcph->seq) - 10000);
    }

    /* Recalculate checksums */
    recalc_checksums(packet_fake, new_packetLen);

    if (set_checksum) {
        /* ...and damage it */
        tcph->check = htons(ntohs(tcph->check) - 1);
    }

    /* Send the fake packet via raw socket */
    raw_socket_send(packet_fake, new_packetLen);
    debug("Fake packet: OK\n");

    return 0;
}

static int send_fake_request(const char *pkt,
                              const unsigned int packetLen,
                              const int is_ipv6,
                              const int is_https,
                              const uint8_t set_ttl,
                              const uint8_t set_checksum,
                              const uint8_t set_seq,
                              const struct fake_t *fake_data
                             ) {
    if (set_ttl) {
        send_fake_data(pkt, packetLen,
                      is_ipv6, is_https,
                      set_ttl, 0, 0,
                      fake_data);
    }
    if (set_checksum) {
        send_fake_data(pkt, packetLen,
                      is_ipv6, is_https,
                      0, set_checksum, 0,
                      fake_data);
    }
    if (set_seq) {
        send_fake_data(pkt, packetLen,
                      is_ipv6, is_https,
                      0, 0, set_seq,
                      fake_data);
    }
    return 0;
}

